package Operadores;

public class clase3 {
	public static void main(String...args) {
		/*
		 * Clase#3 tipos de Variables
		 */
	System.out.println("4 tipos de variables enteras");
		byte v1 =127;
		short v2=12345;
		int v3=10000000;
		long v4=123456789;
		System.out.println("variable byte "+v1);
		System.out.println("variable short "+v2);
		System.out.println("variable int "+v3);
		System.out.println("varibable long "+v4);
	System.out.println("tipos de variables con decimales");
		float v5=3.45f;
		double v6=54000.1234567889;
		System.out.println("variable float "+v5);
		System.out.println("variable double "+v6);
	System.out.println("variables de tipo caracteres");
		char caracter='a';
		boolean decision=true;
		System.out.println("variable caracter "+caracter);
		System.out.println("variable booleana "+decision);
	}
}
